const primeiroValor = 7
const segundoValor = 3

// operador matematico de soma +
console.log(primeiroValor + segundoValor)


// Operador matematico de subtração -
console.log(primeiroValor - segundoValor)


// Operador matematico de divisão /
console.log(primeiroValor / segundoValor)


// Operador matematico de multiplicação *
console.log(primeiroValor * segundoValor)

// Operador matematico de resto da divição %

console.log(primeiroValor % segundoValor)